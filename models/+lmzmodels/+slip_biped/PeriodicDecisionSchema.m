classdef PeriodicDecisionSchema
    %PERIODICDECISIONSCHEMA Exact 12-entry jerboa biped decision ordering.
    methods (Static)
        function schema = create()
            % W1.mat column 30 is a published walking solution and is used
            % only as a deterministic, repository-contained default seed.
            defaults = [0.4146740197904416;0.9453640567805457; ...
                1.1403143903507668e-15;-0.2159761374705677; ...
                -0.4184950212202258;0.2159761374726898; ...
                -0.4184950212198298;0.7795659870967938; ...
                0.2477823903278692;1.806914364516013; ...
                1.275130767747057;2.054696754838414];
            names = {'dx','y','dy','alphaL','dalphaL','alphaR', ...
                'dalphaR','tL_TD','tL_LO','tR_TD','tR_LO','tAPEX'};
            labels = {'Forward velocity','Body height','Vertical velocity', ...
                'Left leg angle','Left leg angular velocity', ...
                'Right leg angle','Right leg angular velocity', ...
                'Left touchdown','Left liftoff','Right touchdown', ...
                'Right liftoff','Apex return / stride period'};
            units = {'m/s','m','m/s','rad','rad/s','rad','rad/s', ...
                's','s','s','s','s'};
            scales = [1;1;0.25;0.5;2;0.5;2;0.5;0.5;0.5;0.5;1];
            specs = lmz.schema.VariableSpec.empty(0,1);
            for index = 1:7
                specs(end+1,1) = lmz.schema.VariableSpec(names{index}, ... %#ok<AGROW>
                    'Label',labels{index},'Group','initial_state', ...
                    'Unit',units{index},'DefaultValue',defaults(index), ...
                    'Scale',scales(index));
            end
            for index = 8:11
                specs(end+1,1) = lmz.schema.VariableSpec(names{index}, ... %#ok<AGROW>
                    'Label',labels{index},'Group','event_timing', ...
                    'Unit',units{index},'DefaultValue',defaults(index), ...
                    'Scale',scales(index),'Topology','cyclic_time', ...
                    'PeriodSource','tAPEX');
            end
            specs(end+1,1) = lmz.schema.VariableSpec(names{12}, ...
                'Label',labels{12},'Group','event_timing','Unit','s', ...
                'DefaultValue',defaults(12),'LowerBound',eps, ...
                'Scale',scales(12),'Topology','positive');
            schema = lmz.schema.VariableSchema(specs,'1.0.0');
        end
    end
end
