classdef PeriodicDecisionSchema
    %PERIODICDECISIONSCHEMA Named 22-entry scientific RoadMap decision.
    methods (Static)
        function schema = create()
            stateNames = {'dx','y','dy','phi','dphi', ...
                'alphaBL','dalphaBL','alphaFL','dalphaFL', ...
                'alphaBR','dalphaBR','alphaFR','dalphaFR'};
            stateLabels = {'Forward velocity','Body height','Vertical velocity', ...
                'Body pitch','Pitch rate','Back-left leg angle', ...
                'Back-left leg angular rate','Front-left leg angle', ...
                'Front-left leg angular rate','Back-right leg angle', ...
                'Back-right leg angular rate','Front-right leg angle', ...
                'Front-right leg angular rate'};
            stateUnits = {'m/s','m','m/s','rad','rad/s','rad','rad/s', ...
                'rad','rad/s','rad','rad/s','rad','rad/s'};
            % Exact PK_20_2 RoadMap column 267 (the manifest default seed).
            defaults = [6.686199620374658;0.9208746540362741; ...
                -5.5966527120104124e-14;2.576949114258376e-13; ...
                5.287987573513577e-14;-9.881524888531251e-13; ...
                5.671908503229492;1.7980427727787824e-12; ...
                5.671908503227983;-2.4947869762897595e-12; ...
                5.671908503229555;3.3046835979316156e-12; ...
                5.6719085032279235];
            scales = [5;1;0.25;0.1;0.1;1;5;1;5;1;5;1;5];
            specs = lmz.schema.VariableSpec.empty(0,1);
            for index = 1:numel(stateNames)
                specs(end+1,1) = lmz.schema.VariableSpec(stateNames{index}, ... %#ok<AGROW>
                    'Label',stateLabels{index},'Group','initial_state', ...
                    'Unit',stateUnits{index},'DefaultValue',defaults(index), ...
                    'Scale',scales(index));
            end
            timingNames = {'tBL_TD','tBL_LO','tFL_TD','tFL_LO', ...
                'tBR_TD','tBR_LO','tFR_TD','tFR_LO'};
            timingLabels = {'Back-left touchdown','Back-left liftoff', ...
                'Front-left touchdown','Front-left liftoff', ...
                'Back-right touchdown','Back-right liftoff', ...
                'Front-right touchdown','Front-right liftoff'};
            timingDefaults = [0.5696992722753348;0.7677082381525054; ...
                0.5696992722749853;0.7677082381520942; ...
                0.5696992722755535;0.7677082381527445; ...
                0.5696992722747665;0.7677082381518553];
            for index = 1:numel(timingNames)
                specs(end+1,1) = lmz.schema.VariableSpec(timingNames{index}, ... %#ok<AGROW>
                    'Label',timingLabels{index},'Group','event_timing', ...
                    'Unit','s','DefaultValue',timingDefaults(index), ...
                    'Scale',0.25,'Topology','cyclic_time', ...
                    'PeriodSource','tAPEX');
            end
            specs(end+1,1) = lmz.schema.VariableSpec('tAPEX', ...
                'Label','Stride period / apex return','Group','event_timing', ...
                'Unit','s','DefaultValue',1.337407510427545, ...
                'LowerBound',eps,'Scale',1,'Topology','positive');
            schema = lmz.schema.VariableSchema(specs,'1.0.0');
        end
    end
end
